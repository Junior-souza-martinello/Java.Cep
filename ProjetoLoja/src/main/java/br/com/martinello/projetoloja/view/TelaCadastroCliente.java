    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        try {
            MetodoCliente metodoCliente = new MetodoCliente();
            ViaCEPClient cliente = new ViaCEPClient();
            Controll controll = new Controll();
            ViaCEPEndereco endereco = cliente.getEndereco(txtCep.getText());
            LocalDate hoje = LocalDate.now();

            metodoCliente.setNome(txtNome.getText());
            metodoCliente.setCpf(txtCpf.getText());
            metodoCliente.setSexo(cobSexo.getSelectedItem().toString()); //ComboBox
            metodoCliente.setDataNascimento(txtData.getText());
            metodoCliente.setCelular(txtCelular.getText());
            metodoCliente.setEmail(txtEmail.getText());
            metodoCliente.setDataExecucao(hoje.format(DateTimeFormatter.ISO_DATE));
            metodoCliente.setCep(txtCep.getText());
            metodoCliente.setComplemento(txtComplemento.getText());
            metodoCliente.setBairro(txtBairro.getText());
            metodoCliente.setLocalidade(txtLocalidade.getText());
            metodoCliente.setUf(txtUf.getText());

            controll.SalvarCliente(metodoCliente);

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Os dsados n√£o foram Gravados");
        }
    }//GEN-LAST:event_btSalvarActionPerformed

